package com.qy._02_class_about._03_variables;

public class _03_InstanceVariables {
    // 实例变量
    private String name;      // 学生姓名
    private int age;          // 学生年龄
    private double score;     // 学生成绩

    // 构造方法
    public _03_InstanceVariables(String name, int age, double score) {
        this.name = name;
        this.age = age;
        this.score = score;
    }
}
